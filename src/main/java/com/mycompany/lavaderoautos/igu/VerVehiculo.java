    private void cargarTabla() {
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }     
        };
        
        String titulos[] = {"Id", "Tipo", "Placa", "Modelo", "Color", "Servicio", "Due√±o", "Celular", "Valor"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        List<Vehiculo> listaVehiculos = control.buscarVehiculos();
        
        if(listaVehiculos != null){
           for(Vehiculo lista : listaVehiculos){
            Object[] objeto = {
                lista.getId(),
                lista.getTipo(),
                lista.getPlaca(),
                lista.getModelo(),
                lista.getColor(),
                lista.getServicio(),
                lista.getDuenio().getNombre(),
                lista.getDuenio().getNumero(),
                lista.getValor()
            };
            modeloTabla.addRow(objeto);
            } 
        }
        
        tablaVehiculos.setModel(modeloTabla);
    }
