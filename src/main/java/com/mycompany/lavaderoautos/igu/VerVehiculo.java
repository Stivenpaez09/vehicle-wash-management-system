    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (tablaVehiculos.getRowCount()>0){
            if(tablaVehiculos.getSelectedRow()!=-1){
                int id = Integer.parseInt(String.valueOf(tablaVehiculos.getValueAt(tablaVehiculos.getSelectedRow(), 0)));
                this.control.eliminarVehiculo(id);
                JOptionPane.showMessageDialog(null, "Vehiculo eliminado exitosamente", "Vehiculo eliminado", JOptionPane.INFORMATION_MESSAGE);
                this.cargarTabla();
            } else {
                JOptionPane.showMessageDialog(null, "Seleciona la celda que deseas eliminar", "Error al eliminar la celda", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No tienes registos para eliminar", "Error al eliminar la celda", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

